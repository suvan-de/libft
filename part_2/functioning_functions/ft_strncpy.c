#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	int i;

	dst = (char *)malloc(len + 1);
	i = 0;
	while (i < len)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
