#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int len;
	int i;

	i = 0;
	len = ft_strlen(src);
	dst = (char *)malloc(len + 1);
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
